	@RequestMapping(value = "/banner")
	@ResponseBody

	public ResponseEntity<PaginationVo> Banner() {

		List<BannerCategory> aa = bannerCategoryMapper.selectall();

		paginationVo.setDataResultList(aa);

		ResponseEntity<PaginationVo> data = new ResponseEntity<PaginationVo>(paginationVo,HttpStatus.OK);

		return data;

	}

